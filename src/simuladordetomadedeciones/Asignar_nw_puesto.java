    public boolean comprobar_existencia(){
        //query para comprobar coincidencias 
        //verificar si un empleado ya tiene un puesto asignado
        String sql = "SELECT EMPLEADOIDPUESTO from PUESTO where EMPLEADOIDPUESTO = "+emp1_id_table1;
        
        Statement st;
        try{
            st = conexion.createStatement();
            ResultSet rs = st.executeQuery(sql); 
            System.out.println(sql);
            if (!rs.next()) {
            // ResultSet esta vacio, y no hay coincidencias. 
            // No existe repeticion de registro
                rs.close();
                st.close();
                id_puesto_dupli = 0;
                 System.out.println("SE hizo la comprobacion y no hay existente");
                 //se retorna un falso denotando que no hay duplicados
                return false;
            }
            int count = rs.getInt(1);
            System.out.println("se hizo la comprobacion exitosa y hay un existene");
            rs.close();
            st.close();
            id_puesto_dupli = count;
            return true; 
            //Esta comparando si count es difernte a 0
            //si hay repeticiones, entonces manda un true
            //si no hay duplicados, entonces manda un false porque SI HAY 0 
        }catch(SQLException e){
             JOptionPane.showMessageDialog(null,"Error. Hubo un problema durante la conexión del servidor. \n"+e.toString());
        } 
    
        return true;
    } 
    
