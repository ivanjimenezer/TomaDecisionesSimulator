    private void jb_asignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_asignarActionPerformed
       int confirmed = JOptionPane.showConfirmDialog(null, 
        "¿Estas seguro de que quieres realizar esta asignación de puestos?:\n ", "Asignación de puestos",
        JOptionPane.YES_NO_OPTION); 
        if (confirmed == JOptionPane.YES_OPTION) {
        //tomo el nombre del puesto de la tabla 2 que ha sido seleccionada
            
            //Se define si el nuevo puesto al que se va a asignar esta vacio
            if(vacios_op){
            //aqui se sabe que el puesto no tenia dueño anterior
                System.out.println("via 1 - el puesto no esta vacio");
            //si la persona ya tiene un puesto y se cambia por otro
                if (existente == true){
                    System.out.println("via 1.1 - el empleado ya tenia puesto");
                    //se reasigna al puesto nuevo del empleado
                    
                    //dejando el anterior vacio  
                    System.out.println("reasignar_existente");
                    reasignar_existente(emp1_id_table1);
                    
                    //Una vedejado vacio el puesto anterior
                    // se envia el id del puesto seleccionado
                    System.out.println("asignar vacio");
                    asignar_vacio(puesto_id_table2);
                    
                }else{
                    System.out.println("via 1.2 - el empleado no tenia puesto antes");
                    //si el empleado seleccionado no tenia puesto, entonces solo se asigna
                    // se envia el id del puesto seleccionado
                    System.out.println("asignar vacio");
                    asignar_vacio(puesto_id_table2);
                    
                }
            }else{
            //Si el nuevo puesto que se asignara ya estaba siendo ocupado
            //Se comprueba si la pesona ya tenia un puesto
                System.out.println("via 2 - el puesto esta vacio");
                if (existente == true){
                    System.out.println("via 2.1 - el empleado tiene puesto yo");
                    //si es asi, el puesto anterior se deja vacio
                    System.out.println("reasignar");
                    reasignar_existente(emp1_id_table1);
                    
                    
                    //y ahora el nuevo puesto se le cambia el empleado
                    System.out.println("reasignar");
                    reasignar(puesto_id_table2);
                    
                }else{ 
                    System.out.println("via 2.2 - el empleado no tenia puesto");
                    //Si el empleado que tendra el nuevo puesto no tiene puesto anterior
                    //se le asigna facilmente el nuevo puesto
                    System.out.println("reasignar");
                    reasignar(puesto_id_table2); 
                    
                } 
               
            } 
             
             mostrar_ocupados(conexion); 
             vacios_op=false;
             select_table2=false;
             existente = comprobar_existencia();
             boton_asig();
        }
         
       
        
    }//GEN-LAST:event_jb_asignarActionPerformed

