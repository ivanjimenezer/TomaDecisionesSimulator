    public void RCombo(String tabla,JComboBox combob, String empleado){
                String sql = "select * from " + tabla+ " where RFCUSUARIO like '%"+empleado+"%'";
                Statement st;
                Conexion con = new Conexion();
                Connection conexion = con.Conectar();
                int id;
                String Descripcion;
                //reinicia el contenido de los items para no repetir
                combob.removeAllItems();
                try{
                    st = conexion.createStatement();
                    ResultSet rs = st.executeQuery(sql);
                    //SE RECORREN LAS FILAS DE LOS DATOS OBTENIDOS
                    while(rs.next()){
                    // SE OBTIENE SU ID
                     id = Integer.parseInt(rs.getString(1));
                     //
                    Descripcion= rs.getString(2);
                    //se guarda el id del usuario dentro de un objeto item
                combob.addItem(new ItemCombo (id,Descripcion));
            } 
        }catch(SQLException e){
                    JOptionPane.showMessageDialog(null,"Error"+e.toString());
                    }
    }
