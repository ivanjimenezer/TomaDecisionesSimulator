    public void RCombo_text(String tabla ,JComboBox combob, String like ){
        like = like.toLowerCase();
        String sql = "select * from " + tabla+" where lower(NOMBRERELACION) like '%"+like+"%'";
        Statement st;
        Conexion con = new Conexion();
        conexion = con.Conectar(); 
        String Descripcion;
        try{
            st = conexion.createStatement();
            ResultSet rs = st.executeQuery(sql);
            jcb_rel.removeAllItems();
            while(rs.next()){ 
                Descripcion= rs.getString(2); 
                jcb_rel.addItem(Descripcion); 
            }  
            rs.close();
            st.close();
        }catch(SQLException e){
                    JOptionPane.showMessageDialog(null,"Error"+e.toString());
                    } 
    }
    
    
