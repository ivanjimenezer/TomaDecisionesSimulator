    private void jb_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_eliminarActionPerformed
      String tipo_relacion = (String) jcb_rel.getSelectedItem();
      int confirmed = JOptionPane.showConfirmDialog(null, 
        "¿Estas seguro de que quieres eliminar este tipo de relación?:\n "+tipo_relacion, "Tipo de relación",
        JOptionPane.YES_NO_OPTION); 
        if (confirmed == JOptionPane.YES_OPTION) {
             EliminarRelacion(tipo_relacion);
             jcb_rel.setSelectedIndex(-1);
            jcb_rel.removeAllItems();
            opciones_relacion_inicio();
        }
      
         
    }//GEN-LAST:event_jb_eliminarActionPerformed

