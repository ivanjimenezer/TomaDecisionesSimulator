    public void IngresarRelacion(int p1,int  p2, String rel){
        // COMPROBAR IGUALDAD DE ID 
        if(p1== p2){
            JOptionPane.showMessageDialog(null,"Error. NO SE PUEDE RELACIONAR A UNA PERSONA CONSIGO MISMO. \n"); 
            return; 
        }
        System.out.println("persona 1:"+p1+"\tpersona 2:"+p2+"\trel: "+rel);
        Statement ps = null;
        Conexion con = new Conexion();
        Connection conexion = con.Conectar();
        Statement st;
        String [] datos = new String[6]; 
        String sql = "Select IDTIPORELACION from TIPORELACION where NOMBRERELACION = '"+rel+"'";
        int id_rel=0,id_max=0;
        //get id de la relacion
        try{
            st = conexion.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while(rs.next()){
                id_rel = Integer.parseInt(rs.getString(1));
                System.out.println("ID DE RELACION : "+id_rel);
            }
        //COMPROBAR REPETICION DE REGISTRO
            String sql_dupli = "Select * from RELACION where TIPORELACION_IDTIPORELACION = "+id_rel+
                " AND EMPLEADOS_IDEMPLEADOS = "+p1+" AND EMPLEADOS_IDEMPLEADOS1 = "+p2;
            st = conexion.createStatement();
            rs = st.executeQuery(sql_dupli);
            System.out.println(sql_dupli);
            if (rs.next()) {
            // ResultSet is empty
                rs.close();
                st.close();
                JOptionPane.showMessageDialog(null,"Error. La relacion ya fue creada. \n");
                return;
            }     
        
        
        //AGREGAR
            //Conseguir valor ultimo de la tabla
        String sql2 = "SELECT max(IDRELACION) FROM RELACION";
            System.out.println(sql2);
        rs = st.executeQuery(sql2);
            while(rs.next()){
                
                System.out.println(rs.getString(1));
                
                if (rs.getString(1)==null) {
                    id_max=0;
                }else{
                  id_max = Integer.parseInt(rs.getString(1));

                }
                System.out.println("Id relacion from relacion mas grande: "+id_max);
            }
        //INSERTAR VALORES EN LA TABLA DE RELACIONES
        
          
 
       id_max=  id_max+1;
       
       
       String sql3 = "INSERT INTO RELACION  VALUES ("+id_max+", "+id_rel+", "+p1+","+p2+")";
       System.out.println("Insertar relacion: "+sql3);
       System.out.println(sql3);
       ps = conexion.createStatement(); 
       ps.execute(sql3);
       rs.close();
       st.close();
       ps.close();
        }catch(SQLException e){
                    JOptionPane.showMessageDialog(null,"Error: NO EXISTE ESA RELACION  "+e.toString());
                    }  
    } 
